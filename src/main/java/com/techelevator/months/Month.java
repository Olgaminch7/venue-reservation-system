package com.techelevator.months;

public class Month {
	
	private String name;
	private int numberOfMonth;
	
	public Month(String name, int number) {
		this.name = name;
		this.numberOfMonth = number;
	}

	public String getName() {
		return name;
	}

	public int getNumberOfMonth() {
		return numberOfMonth;
	}
	
	

}
